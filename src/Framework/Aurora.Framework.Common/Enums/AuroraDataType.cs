namespace Aurora.Framework
{
    /// <summary>
    /// Define las opciones de tipo de dato para configuración de atributos y parámetros.
    /// </summary>
    public enum AuroraDataType
    {
        /// <summary>
        /// Tipo de dato binario.
        /// </summary>
        Boolean,

        /// <summary>
        /// Tipo de dato catálogo.
        /// </summary>
        Catalog,

        /// <summary>
        /// Tipo de dato entero.
        /// </summary>
        Integer,
        
        /// <summary>
        /// Tipo de dato moneda.
        /// </summary>
        Money,

        /// <summary>
        /// Tipo de datos numérico o decimal.
        /// </summary>
        Numeric,

        /// <summary>
        /// Tipo de datos texto.
        /// </summary>
        Text
    }
}