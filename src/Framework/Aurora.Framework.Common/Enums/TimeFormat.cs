namespace Aurora.Framework
{
    /// <summary>
    /// Define las opciones de formato de hora para un objeto System.TimeSpan.
    /// </summary>
    public enum TimeFormat
    {
        /// <summary>
        /// Formato hora-minuto.
        /// </summary>
        HourMinute,

        /// <summary>
        /// Formato hora-minuto-segundo.
        /// </summary>
        HourMinuteSecond,

        /// <summary>
        /// Formato hora-minuto-milisegundos.
        /// </summary>
        HourMinuteMillisecond
    }
}